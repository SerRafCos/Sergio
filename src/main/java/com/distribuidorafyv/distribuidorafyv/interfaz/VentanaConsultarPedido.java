    private void btnCancelarPedido1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarPedido1ActionPerformed
        if (tablaPedido.getRowCount() > 0) {

            if (tablaPedido.getSelectedRow() != -1) {

                int nroPedido = Integer.parseInt(String.valueOf(tablaPedido.getValueAt(tablaPedido.getSelectedRow(), 0)));

                controladora.eliminarLogicamentePedido(nroPedido);

                mostrarMensaje("Proveedor eliminado");

                cargarTabla();

            } else {
                mostrarError("No se selecciono ningun registro para eliminar");
            }

        } else {

            mostrarError("No existen proveedores para eliminar");

        }
    }//GEN-LAST:event_btnCancelarPedido1ActionPerformed

private void cargarTabla() {
       
        
        //tabla no editable
        DefaultTableModel modeloTabla = new DefaultTableModel()
                {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    
                    }
                };
        
        String titulos[] = {"NroPedido", "Dni Cliente", "Fecha", "Total", "Activo"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //traer productos desde la base
        List <Pedido> listaPedido = controladora.obtenerPedido();
        
        
        if (listaPedido != null) {
             
            for (Pedido pedido : listaPedido) {
            
                Object[] object = {pedido.getNroPedido(), pedido.getDni(), pedido.getFecha(), pedido.getTotal(), pedido.getActivo()} ;
                
                modeloTabla.addRow(object);
            
            }
            
        }
        
        tablaPedido.setModel(modeloTabla);
        
    }    
    
