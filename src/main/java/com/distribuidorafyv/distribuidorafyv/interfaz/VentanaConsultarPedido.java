    private void btnDetallePedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallePedidoActionPerformed
        if (tablaPedido.getRowCount() > 0) {

            if (tablaPedido.getSelectedRow() != -1) {

                int idPedido = Integer.parseInt(String.valueOf(tablaPedido.getValueAt(tablaPedido.getSelectedRow(), 0)));

                VentanaDetallePedido detalle = new VentanaDetallePedido(idPedido);
                detalle.setVisible(true);
                detalle.setLocationRelativeTo(null);


            } else {
                mostrarError("No se selecciono ningun registro para modificar");
            }

        } else {

            mostrarError("No existen productos para modificar");

        }
    }//GEN-LAST:event_btnDetallePedidoActionPerformed

